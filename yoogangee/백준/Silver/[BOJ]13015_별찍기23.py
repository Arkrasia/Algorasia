n = int(input())

print('*'*n + ' '*(2*n-3) + '*'*n)

for i in range(n-2):
    print(' '*(i+1) + '*' + ' '*(n-2) + '*' + ' '*(2*(n-2-i)-1) + '*' + ' '*(n-2) + '*')

print(' '*(n-1) + '*' + ' '*(n-2) + '*' + ' '*(n-2) + '*')

for i in range(n-3, -1, -1):
    print(' '*(i+1) + '*' + ' '*(n-2) + '*' + ' '*(2*(n-2-i)-1) + '*' + ' '*(n-2) + '*')

print('*'*n + ' '*(2*n-3) + '*'*n)
